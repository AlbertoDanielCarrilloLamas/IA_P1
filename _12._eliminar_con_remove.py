# Elimina de la siguiente lista los elementos 'amarillo' y 'rojo'.
# Solo puedes eliminarlos utilizando el método remove().

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
#declaramos lista

colores.remove('amarillo') #eliminamos el amarillo
colores.remove("rojo") #eliminamos el rojo

print(colores) #corroboramos que se hayan eliminado los elementos